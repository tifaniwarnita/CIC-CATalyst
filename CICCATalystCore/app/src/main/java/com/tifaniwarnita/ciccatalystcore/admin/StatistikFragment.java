        // Inflate the layout for this fragment
        View v = inflater.inflate(R.layout.fragment_statistik, container, false);

//        tvX = (TextView) v.findViewById(R.id.tvXMax);
//        tvY = (TextView) v.findViewById(R.id.tvYMax);

